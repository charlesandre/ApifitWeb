<?php

namespace AppBundle\Entity;

/**
 * TableDefi
 */
class TableDefi
{
    /**
     * @var integer
     */
    private $did;

    /**
     * @var integer
     */
    private $level;

    /**
     * @var integer
     */
    private $xp;

    /**
     * @var string
     */
    private $sport;

    /**
     * @var string
     */
    private $content;

    /**
     * @var integer
     */
    private $progress;


    /**
     * Get did
     *
     * @return integer
     */
    public function getDid()
    {
        return $this->did;
    }

    /**
     * Set level
     *
     * @param integer $level
     *
     * @return TableDefi
     */
    public function setLevel($level)
    {
        $this->level = $level;

        return $this;
    }

    /**
     * Get level
     *
     * @return integer
     */
    public function getLevel()
    {
        return $this->level;
    }

    /**
     * Set xp
     *
     * @param integer $xp
     *
     * @return TableDefi
     */
    public function setXp($xp)
    {
        $this->xp = $xp;

        return $this;
    }

    /**
     * Get xp
     *
     * @return integer
     */
    public function getXp()
    {
        return $this->xp;
    }

    /**
     * Set sport
     *
     * @param string $sport
     *
     * @return TableDefi
     */
    public function setSport($sport)
    {
        $this->sport = $sport;

        return $this;
    }

    /**
     * Get sport
     *
     * @return string
     */
    public function getSport()
    {
        return $this->sport;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return TableDefi
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * Set progress
     *
     * @param integer $progress
     *
     * @return TableDefi
     */
    public function setProgress($progress)
    {
        $this->progress = $progress;

        return $this;
    }

    /**
     * Get progress
     *
     * @return integer
     */
    public function getProgress()
    {
        return $this->progress;
    }
}
