<?php

namespace AppBundle\Entity;

/**
 * UsersAccounts
 */
class UsersAccounts
{
    /**
     * @var integer
     */
    private $aid;

    /**
     * @var integer
     */
    private $uid;

    /**
     * @var string
     */
    private $api_id;

    /**
     * @var string
     */
    private $name_account;

    /**
     * @var string
     */
    private $brand;


    /**
     * Get aid
     *
     * @return integer
     */
    public function getAid()
    {
        return $this->aid;
    }

    /**
     * Set uid
     *
     * @param integer $uid
     *
     * @return UsersAccounts
     */
    public function setUid($uid)
    {
        $this->uid = $uid;

        return $this;
    }

    /**
     * Get uid
     *
     * @return integer
     */
    public function getUid()
    {
        return $this->uid;
    }

    /**
     * Set apiId
     *
     * @param string $apiId
     *
     * @return UsersAccounts
     */
    public function setApiId($apiId)
    {
        $this->api_id = $apiId;

        return $this;
    }

    /**
     * Get apiId
     *
     * @return string
     */
    public function getApiId()
    {
        return $this->api_id;
    }

    /**
     * Set nameAccount
     *
     * @param string $nameAccount
     *
     * @return UsersAccounts
     */
    public function setNameAccount($nameAccount)
    {
        $this->name_account = $nameAccount;

        return $this;
    }

    /**
     * Get nameAccount
     *
     * @return string
     */
    public function getNameAccount()
    {
        return $this->name_account;
    }

    /**
     * Set brand
     *
     * @param string $brand
     *
     * @return UsersAccounts
     */
    public function setBrand($brand)
    {
        $this->brand = $brand;

        return $this;
    }

    /**
     * Get brand
     *
     * @return string
     */
    public function getBrand()
    {
        return $this->brand;
    }
}
